# Arithmetic Operations
a = 10
b = 5

c = a + b  # Addition
d = a - b  # Subtraction
e = a * b  # Multiplication
f = a / b  # Division
g = a % b  # Modulus
h = a ** b # Exponentiation

print("addition:", c)
print("subtraction:", d)
print("multiplication:", e)
print("division:", f)
print("modulus:", g)
print("exponentiation:", h)